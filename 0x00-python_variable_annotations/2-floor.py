#!/usr/bin/env python3
"""
Basic Annotations - floor

This module contains a function `floor` that takes a float argument and
returns the floor value as an integer.
"""


def floor(n: float) -> int:
    """
    Return the floor of a float as an integer.

    Args:
        n (float): The float number to be floored.

    Returns:
        int: The floor value of n.
    """
    return int(n)
